$result = 1 -eq 2
Write-Host "1 equals 2 is $result"

$result = 1 -ne 2
Write-Host "1 not equals 2 is $result"

$result = 1 -gt 2
Write-Host "1 larger than 2 is $result"

$result = 1 -ge 2
Write-Host "1 larger than or equals 2 is $result"

$result = 1 -lt 2
Write-Host "1 less than 2 is $result"

$result = 1 -le 2
Write-Host "1 less than or equals 2 is $result"